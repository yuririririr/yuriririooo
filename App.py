import streamlit as st

st.title("📘 유리함수 교과서")
st.write("이 웹은 고1 수준에서 유리함수를 이해하기 쉽게 정리한 교재입니다.")

st.header("1️⃣ 유리함수의 정의")
st.write("""
유리함수는 '분수가 있는 함수'라고 생각하면 돼.
정확하게 말하면 **분자와 분모가 모두 다항식인 함수**야.

예:  
f(x) = (x + 1) / (x - 2)

이 함수에서 x = 2일 때는 0으로 나누기가 돼서 정의되지 않아.
그래서 x = 2에서 그래프가 '끊어져' (점근선).
""")

st.header("2️⃣ 직접 값 계산해보기")
x = st.slider("x 값을 골라봐 (2는 피해야 해)", -10, 10, 0)
y = "정의되지 않음 (0으로 나눔)" if x == 2 else (x + 1) / (x - 2)
st.write(f"f({x}) = (x + 1) / (x - 2) = {y}")

st.header("✏️ 오늘 공부한 것 적어보기")
memo = st.text_area("배운 내용을 한 문장으로 요약해봐:")
st.write("너의 요약:", memo)
